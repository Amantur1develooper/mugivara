"""
Gunicorn production config — webordo.kg
Применять: gunicorn -c gunicorn.conf.py config.wsgi:application
"""
import multiprocessing

# ── Воркеры ───────────────────────────────────────────────────────────────────
# 2-4 воркера на 1-2 CPU; для слабого VPS ставь 2
workers = 2
worker_class = "sync"
threads = 2                     # 2 потока на воркер → 4 одновременных запроса

# ── Таймауты ──────────────────────────────────────────────────────────────────
timeout = 120                   # 120 сек до 500 (вместо дефолтных 30)
graceful_timeout = 30           # сколько ждать завершения текущих запросов
keepalive = 5                   # держать keep-alive соединение 5 сек

# ── Привязка ──────────────────────────────────────────────────────────────────
bind = "127.0.0.1:8000"

# ── Логи ──────────────────────────────────────────────────────────────────────
accesslog = "-"                 # stdout
errorlog  = "-"                 # stdout
loglevel  = "warning"          # только ошибки в продакшене
access_log_format = '%(h)s "%(r)s" %(s)s %(b)s %(M)sms'

# ── Производительность ────────────────────────────────────────────────────────
worker_tmp_dir = "/dev/shm"    # RAM-диск для tmp файлов воркеров (быстрее)
max_requests = 1000             # рестартовать воркер каждые 1000 запросов (чистит утечки памяти)
max_requests_jitter = 100       # случайный разброс чтобы не все рестартовали одновременно

# ── Preload ───────────────────────────────────────────────────────────────────
preload_app = True              # загружает Django один раз до форка — экономит RAM
